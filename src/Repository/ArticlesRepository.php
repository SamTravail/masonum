class ArticlesRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Articles::class);
    }
    public function findPublicArticle(?int $nbArticles): array
    {
        $queryBuilder = $this->createQueryBuilder('r')
            //   language sql true = 1 false =0
            ->where('r.isPublic = 1')
            // classer par ordre decroissant date
            ->orderBy('r.createdAt', 'DESC');
        // mettre un nombre max d'articles si le nbArticle est différent de 0
        if ($nbArticles !== 0 || $nbArticles !== null) {
            $queryBuilder->setMaxResults($nbArticles);
        }
        // recuperé la query
        return $queryBuilder->getQuery()
            // récupérer de la query le resultat
            ->getResult();
    }
    public function add(Articles $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Articles $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

